package com.agent.czb.core.sys.model;

import java.util.Date;

public class NewParkWhiteList {
	private Long id;//停车场ID
	private String park_code; //停车场编码
	private String plate_num; //车牌号
	private String phone;//手机号
	private Date start_time;
	private Date end_time;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getPhone() {
		return phone;
	}
	public Date getStart_time() {
		return start_time;
	}
	public void setStart_time(Date start_time) {
		this.start_time = start_time;
	}
	public Date getEnd_time() {
		return end_time;
	}
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPark_code() {
		return park_code;
	}
	public void setPark_code(String park_code) {
		this.park_code = park_code;
	}
	public String getPlate_num() {
		return plate_num;
	}
	public void setPlate_num(String plate_num) {
		this.plate_num = plate_num;
	}
	@Override
	public String toString() {
		return "NewParkWhiteList [id=" + id + ", park_code=" + park_code + ", plate_num=" + plate_num + ", phone="
				+ phone + ", start_time=" + start_time + ", end_time=" + end_time + "]";
	}
	
	
	
}
