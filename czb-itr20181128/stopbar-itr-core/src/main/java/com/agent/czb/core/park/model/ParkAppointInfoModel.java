package com.agent.czb.core.park.model;

import java.io.Serializable;

/**
 * 停车场预约信息模型层
 */
public class ParkAppointInfoModel implements Serializable { // 停车场预约信息
	private Long id;//标识
	private Long userId;//用户标识
	private String userName;//用户姓名
	private String parkCode;//停车场编码
	private String plateNum;//车牌号码
	private String state;//预约状态
	private java.util.Date time;//预约时间
	private Long totalPrice;//金额
	private java.util.Date createTime;//创建时间
	private java.util.Date updateTime;//修改时间

	public Long getId() {
		return id;
	}

 	public void setId(Long id) {
		this.id = id;
	}

	public Long getUserId() {
		return userId;
	}

 	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

 	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getParkCode() {
		return parkCode;
	}

 	public void setParkCode(String parkCode) {
		this.parkCode = parkCode;
	}

	public String getPlateNum() {
		return plateNum;
	}

 	public void setPlateNum(String plateNum) {
		this.plateNum = plateNum;
	}

	public String getState() {
		return state;
	}

 	public void setState(String state) {
		this.state = state;
	}

	public java.util.Date getTime() {
		return time;
	}

 	public void setTime(java.util.Date time) {
		this.time = time;
	}

	public Long getTotalPrice() {
		return totalPrice;
	}

 	public void setTotalPrice(Long totalPrice) {
		this.totalPrice = totalPrice;
	}

	public java.util.Date getCreateTime() {
		return createTime;
	}

 	public void setCreateTime(java.util.Date createTime) {
		this.createTime = createTime;
	}

	public java.util.Date getUpdateTime() {
		return updateTime;
	}

 	public void setUpdateTime(java.util.Date updateTime) {
		this.updateTime = updateTime;
	}

}
